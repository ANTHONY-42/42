void print_stack(t_stack *stack)
{
    t_node *current = stack->head;
    while (current)
    {
        printf("%d\n", current->value);
        current = current->next;
    }
}

int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	result;

	i = 0;
	sign = 1;
	result = 0;

	while (str[i] <= ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}

	while (str[i] >= '0' && str[i] <= '9')
	{
		result  = result * 10 + (str[i] - '0');
			i++;
	}
	return (result * sign);
}

